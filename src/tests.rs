use crate::mdf3;
#[cgf(test)]
mod tests {
	#[test]
	fn idblock(){
		let id_data = [
			0x4D, 0x44, 0x46, 0x20, 
			0x20, 0x20, 0x20, 0x20, 
			0x33, 0x2E, 0x33, 0x30, 
			0x00, 0x00, 0x00, 0x00, 
			0x61, 0x6D, 0x64, 0x66, 
			0x36, 0x34, 0x34, 0x00, 
			0x00, 0x00, 0x00, 0x00, 
			0x4A, 0x01, 0x00, 0x00, 
			0x00, 0x00, 0x00, 0x00, 
			0x00, 0x00, 0x00, 0x00, 
			0x00, 0x00, 0x00, 0x00, 
			0x00, 0x00, 0x00, 0x00, 
			0x00, 0x00, 0x00, 0x00, 
			0x00, 0x00, 0x00, 0x00, 
			0x00, 0x00, 0x00, 0x00, 
			0x00, 0x00, 0x00, 0x00,
		];

		let (id_block, position, endian) = mdf3::IDBLOCK::read(&id_data);

		asserteq!(position, 64);
		asserteq!(endian, true);
		assert!(utils::eq(&id_block.format_id, &[0x33, 0x2E, 0x33, 0x30, 
												0x00, 0x00, 0x00, 0x00,]));
		assert!(utils::eq(&id_block.program_id, &[0x61, 0x6D, 0x64, 0x66, 
												0x36, 0x34, 0x34, 0x00, ]));
		asserteq!(id_block.default_float_format, 0);
		asserteq!(id_block.version_number, 330);
		asserteq!(id_block.code_page_number, 0);
		
	}
}